      <h2>Objeto</h2>
      <p>El presente documento establece las condiciones de uso del portal web de la División de
      Estudios de Posgrado de la Facultad de Economía, Contaduría y Administración de la UJED.
      El acceso y navegación en este sitio implica la aceptación plena de estos términos.</p>
