    <div class="legal-block">
      <h2>Uso del Contenido</h2>
      <p>Todo el contenido publicado —textos, imágenes, documentos, logotipos e identidad
      gráfica— es propiedad de la UJED o de sus respectivos titulares y está protegido por
      la legislación mexicana en materia de derechos de autor. Queda prohibida su reproducción
      total o parcial sin autorización expresa por escrito.</p>
      <p>Se permite la descarga de documentos y convocatorias para uso personal e informativo,
      siempre que se cite la fuente.</p>
    </div>

    <div class="legal-block">
      <h2>Limitación de Responsabilidad</h2>
      <p>La División de Estudios de Posgrado FECA UJED no se hace responsable de:</p>
      <ul>
        <li>Errores u omisiones en la información publicada</li>
        <li>Interrupciones o fallas técnicas del servicio</li>
        <li>El contenido de sitios externos enlazados desde este portal</li>
        <li>El uso que terceros hagan de la información publicada</li>
        <li>Daños o perjuicios derivados del uso inadecuado del portal</li>
      </ul>
    </div>

    <div class="legal-block">
      <h2>Acceso y Disponibilidad</h2>
      <p>El acceso al portal es gratuito. La División se reserva el derecho de modificar,
      suspender o interrumpir el servicio en cualquier momento, ya sea por razones técnicas,
      de mantenimiento o institucionales, sin necesidad de previo aviso.</p>
    </div>

    <div class="legal-block">
      <h2>Conducta del Usuario</h2>
      <p>El usuario se compromete a utilizar este portal de manera responsable y conforme a la ley,
      absteniéndose de:</p>
      <ul>
        <li>Reproducir, distribuir o modificar el contenido sin autorización</li>
        <li>Utilizar el sitio para fines ilícitos o contrarios a la ética académica</li>
        <li>Intentar acceder de manera no autorizada a sistemas o datos del portal</li>
      </ul>
    </div>

